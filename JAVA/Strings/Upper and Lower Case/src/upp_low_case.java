/*
 * ############################################################################
 * ############################################################################
 * 
 * 				This program demonstrates the use of the
 * 		  toUpperCase and toLowerCase string functions in JAVA.
 * 
 * ############################################################################
 * ############################################################################
*/

public class upp_low_case {

	public static void main(String[] args) {
		String str = "Hi, To You There.";
		
		System.out.println(" " + str.toLowerCase()); 						// This will print the string in Lower case
		System.out.println("\n " + str.toUpperCase()); 						// This will print the string in Upper case
	}

}
